// Função para rolar o carrossel de Sushis Crus para a esquerda
function scrollCarouselLeftCrus() {
    const carouselCrus = document.querySelector('#carousel-crus .carousel');
    carouselCrus.scrollBy({
        left: -300, // A quantidade de pixels para rolar para a esquerda
        behavior: 'smooth'
    });
}

